#[test]
fn full_login_form_guard() {
    let decl = make_guard(
        "LoginForm",
        vec![
            make_field("email", "string", vec![("trim", vec![]), ("email", vec![])]),
            make_field(
                "password",
                "string",
                vec![("minLen", vec![int_lit(8)]), ("maxLen", vec![int_lit(100)])],
            ),
        ],
    );
    let (out, meta) = emit_js(&decl);

    // File header
    assert!(
        out.contains("Guard validator: `LoginForm`."),
        "should have header: {out}"
    );
    assert!(
        out.contains("Generated by GaleX compiler"),
        "should have gen notice: {out}"
    );

    // Email regex constant
    assert!(
        out.contains("const EMAIL_RE = /^"),
        "should have EMAIL_RE const: {out}"
    );

    // Sanitize function (trim exists)
    assert!(
        out.contains("export function sanitizeLoginForm(data)"),
        "should have sanitize fn: {out}"
    );
    assert!(
        out.contains("out.email = out.email.trim()"),
        "should trim email: {out}"
    );

    // Validate function
    assert!(
        out.contains("export function validateLoginForm(data)"),
        "should have validate fn: {out}"
    );
    assert!(
        out.contains("!EMAIL_RE.test(data.email)"),
        "should check email format: {out}"
    );
    assert!(
        out.contains("data.password.length < 8"),
        "should check minLen: {out}"
    );
    assert!(
        out.contains("data.password.length > 100"),
        "should check maxLen: {out}"
    );

    // Return structure
    assert!(
        out.contains("return errors.length === 0"),
        "should have return: {out}"
    );
    assert!(
        out.contains("{ ok: true, data }"),
        "should return ok on success: {out}"
    );
    assert!(
        out.contains("{ ok: false, errors }"),
        "should return errors on failure: {out}"
    );

    // Meta
    assert_eq!(meta.guard_name, "LoginForm");
    assert_eq!(meta.module_name, "login_form");
    assert_eq!(meta.validate_fn, "validateLoginForm");
    assert_eq!(meta.sanitize_fn.as_deref(), Some("sanitizeLoginForm"));
    assert_eq!(meta.fields, vec!["email", "password"]);
}

#[test]
fn guard_with_all_numeric_validators() {
    let decl = make_guard(
        "NumericForm",
        vec![
            make_field(
                "age",
                "int",
                vec![("min", vec![int_lit(0)]), ("max", vec![int_lit(150)])],
            ),
            make_field(
                "score",
                "float",
                vec![("range", vec![int_lit(0), int_lit(100)])],
            ),
            make_field("count", "int", vec![("positive", vec![])]),
            make_field("balance", "float", vec![("nonNegative", vec![])]),
            make_field("qty", "float", vec![("integer", vec![])]),
        ],
    );
    let (out, _) = emit_js(&decl);

    assert!(out.contains("data.age < 0"), "min(0): {out}");
    assert!(out.contains("data.age > 150"), "max(150): {out}");
    assert!(
        out.contains("data.score < 0 || data.score > 100"),
        "range(0,100): {out}"
    );
    assert!(out.contains("data.count <= 0"), "positive: {out}");
    assert!(out.contains("data.balance < 0"), "nonNegative: {out}");
    assert!(
        out.contains("!Number.isInteger(data.qty)"),
        "integer: {out}"
    );
}

#[test]
fn guard_with_all_string_validators() {
    let decl = make_guard(
        "StringForm",
        vec![
            make_field("name", "string", vec![("minLen", vec![int_lit(2)])]),
            make_field("bio", "string", vec![("maxLen", vec![int_lit(500)])]),
            make_field("title", "string", vec![("nonEmpty", vec![])]),
            make_field("email", "string", vec![("email", vec![])]),
            make_field("website", "string", vec![("url", vec![])]),
            make_field("token", "string", vec![("uuid", vec![])]),
            make_field(
                "role",
                "string",
                vec![("oneOf", vec![str_lit("admin"), str_lit("user")])],
            ),
            make_field(
                "code",
                "string",
                vec![("regex", vec![str_lit(r"^[A-Z]+$")])],
            ),
        ],
    );
    let (out, _) = emit_js(&decl);

    assert!(out.contains("data.name.length < 2"), "minLen: {out}");
    assert!(out.contains("data.bio.length > 500"), "maxLen: {out}");
    assert!(out.contains("data.title.length === 0"), "nonEmpty: {out}");
    assert!(out.contains("!EMAIL_RE.test(data.email)"), "email: {out}");
    assert!(out.contains("!URL_RE.test(data.website)"), "url: {out}");
    assert!(out.contains("!UUID_RE.test(data.token)"), "uuid: {out}");
    assert!(
        out.contains(r#"!["admin", "user"].includes(data.role)"#),
        "oneOf: {out}"
    );
    assert!(out.contains(".test(data.code)"), "regex: {out}");
}

