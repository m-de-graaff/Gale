//! Integration tests for JS guard validation code generation (Phase 12.3).
//!
//! Tests construct AST nodes directly and verify the generated JavaScript
//! contains expected patterns for client-side validation functions.

use galex::ast::*;
use galex::codegen::emit_form_js;
use galex::codegen::emit_guard_js::{self, GuardJsMeta};
use galex::codegen::js_emitter::JsEmitter;
use galex::codegen::rust_emitter::RustEmitter;
use galex::codegen::CodegenContext;
use galex::span::Span;
use galex::types::ty::TypeInterner;

fn s() -> Span {
    Span::dummy()
}

fn make_guard(name: &str, fields: Vec<GuardFieldDecl>) -> GuardDecl {
    GuardDecl {
        name: name.into(),
        fields,
        span: s(),
    }
}

fn make_field(name: &str, ty: &str, validators: Vec<(&str, Vec<Expr>)>) -> GuardFieldDecl {
    GuardFieldDecl {
        name: name.into(),
        ty: TypeAnnotation::Named {
            name: ty.into(),
            span: s(),
        },
        validators: validators
            .into_iter()
            .map(|(vname, args)| ValidatorCall {
                name: vname.into(),
                args,
                span: s(),
            })
            .collect(),
        span: s(),
    }
}

fn int_lit(val: i64) -> Expr {
    Expr::IntLit {
        value: val,
        span: s(),
    }
}

fn str_lit(val: &str) -> Expr {
    Expr::StringLit {
        value: val.into(),
        span: s(),
    }
}

fn emit_js(decl: &GuardDecl) -> (String, GuardJsMeta) {
    let mut e = JsEmitter::new();
    let meta = emit_guard_js::emit_guard_js_file(&mut e, decl);
    (e.finish(), meta)
}

fn make_program(items: Vec<Item>) -> Program {
    Program { items, span: s() }
}

