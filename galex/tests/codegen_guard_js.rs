#[test]
fn ssr_form_guard_emits_data_attribute() {
    use galex::codegen::hydration::HydrationCtx;
    use galex::codegen::ssr;

    let mut e = RustEmitter::new();
    let mut h = HydrationCtx::new();
    let nodes = vec![TemplateNode::Element {
        tag: "form".into(),
        attributes: vec![],
        directives: vec![
            Directive::FormAction {
                action: Expr::Ident {
                    name: "login".into(),
                    span: s(),
                },
                span: s(),
            },
            Directive::FormGuard {
                guard: Expr::Ident {
                    name: "LoginForm".into(),
                    span: s(),
                },
                span: s(),
            },
        ],
        children: vec![],
        span: s(),
    }];
    ssr::emit_template_nodes(&mut e, &nodes, &mut h, None);
    let output = e.finish();

    // Should have data-gale-guard attribute on the form
    assert!(
        output.contains("data-gale-guard=\\\"LoginForm\\\""),
        "data-gale-guard attr: {output}"
    );
    // Should still have hidden input for server-side
    assert!(output.contains("__guard"), "hidden input: {output}");
    // Should have wiring script
    assert!(
        output.contains("script type=\\\"module\\\""),
        "wiring script: {output}"
    );
    assert!(
        output.contains("validateLoginForm"),
        "validate fn import: {output}"
    );
    assert!(output.contains("gale-forms.js"), "runtime import: {output}");
}

