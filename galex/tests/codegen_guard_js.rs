#[test]
fn codegen_find_js_guard_meta() {
    let interner = TypeInterner::new();
    let program = make_program(vec![Item::GuardDecl(make_guard(
        "TestGuard",
        vec![
            make_field(
                "name",
                "string",
                vec![("trim", vec![]), ("minLen", vec![int_lit(1)])],
            ),
            make_field("age", "int", vec![("min", vec![int_lit(0)])]),
        ],
    ))]);
    let mut ctx = CodegenContext::new(&interner, "test_app");
    ctx.emit_program(&program);

    let meta = ctx.find_js_guard_meta("TestGuard");
    assert!(meta.is_some(), "should find guard meta");
    let meta = meta.unwrap();
    assert_eq!(meta.validate_fn, "validateTestGuard");
    assert_eq!(meta.sanitize_fn.as_deref(), Some("sanitizeTestGuard"));
    assert_eq!(meta.fields, vec!["name", "age"]);

    assert!(
        ctx.find_js_guard_meta("NonExistent").is_none(),
        "should return None for unknown guard"
    );
}
