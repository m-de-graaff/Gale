#[test]
fn guard_sanitize_trim_and_precision() {
    let decl = make_guard(
        "G",
        vec![
            make_field("name", "string", vec![("trim", vec![])]),
            make_field("price", "float", vec![("precision", vec![int_lit(2)])]),
        ],
    );
    let (out, meta) = emit_js(&decl);

    assert!(
        out.contains("export function sanitizeG(data)"),
        "sanitize fn: {out}"
    );
    assert!(
        out.contains("const out = { ...data }"),
        "shallow clone: {out}"
    );
    assert!(out.contains("out.name = out.name.trim()"), "trim: {out}");
    assert!(
        out.contains("Math.round(out.price * 100) / 100"),
        "precision(2): {out}"
    );
    assert!(out.contains("return out;"), "returns sanitized data: {out}");
    assert!(meta.sanitize_fn.is_some());
}

