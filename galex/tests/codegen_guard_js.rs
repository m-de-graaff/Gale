#[test]
fn guard_error_messages_match_rust_side() {
    // Verify that JS error messages are identical to Rust-side messages
    let decl = make_guard(
        "G",
        vec![
            make_field("a", "int", vec![("min", vec![int_lit(5)])]),
            make_field("b", "int", vec![("max", vec![int_lit(100)])]),
            make_field("c", "int", vec![("range", vec![int_lit(1), int_lit(10)])]),
            make_field("d", "string", vec![("minLen", vec![int_lit(3)])]),
            make_field("e", "string", vec![("maxLen", vec![int_lit(50)])]),
            make_field("f", "string", vec![("email", vec![])]),
            make_field("g", "string", vec![("url", vec![])]),
            make_field("h", "string", vec![("uuid", vec![])]),
            make_field("i", "string", vec![("nonEmpty", vec![])]),
            make_field("j", "int", vec![("positive", vec![])]),
            make_field("k", "float", vec![("nonNegative", vec![])]),
            make_field("l", "float", vec![("integer", vec![])]),
        ],
    );
    let (out, _) = emit_js(&decl);

    let expected_messages = [
        "must be at least 5",
        "must be at most 100",
        "must be between 1 and 10",
        "must be at least 3 character(s)",
        "must be at most 50 character(s)",
        "must be a valid email address",
        "must be a valid URL",
        "must be a valid UUID",
        "must not be empty",
        "must be positive",
        "must not be negative",
        "must be a whole number",
    ];
    for msg in &expected_messages {
        assert!(out.contains(msg), "missing message '{msg}': {out}");
    }
}

